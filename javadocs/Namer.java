package HDT8;
/**
 * Gets y sets de namer
 */
public class Namer {
    
// Declaring the variables that will be used in the program.
    protected String nombrep;
    protected String nombreu;
    protected int vnice;

/**
 * This function returns the value of the variable nombrep
 * 
 * @return The value of the nombrep variable.
 */
    public String getNombrep() {
        return this.nombrep;
    }

/**
 * This function returns the value of the variable nombreu
 * 
 * @return The value of the nombreu variable.
 */
    public String getNombreu() {
        return this.nombreu;
    }

/**
 * It returns the value of the variable vnice.
 * 
 * @return The value of the variable vnice.
 */
    public int getVnice() {
        return this.vnice;
    }
}
